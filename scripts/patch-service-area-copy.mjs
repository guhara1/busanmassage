function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

for (const [title, copy] of copyByTitle) {
  const pattern = new RegExp(`(<h3>${escapeRegExp(title)}<\\/h3><p>)[\\s\\S]*?(<\\/p>)`, "g");
  html = html.replace(pattern, `$1${copy}$2`);
}

writeFileSync(file, html, "utf8");
